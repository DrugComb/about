<h3>Background</h3>
                                                <p align="justify">Making cancer treatment more personalized and effective is one of the grand challenges in our health care system.
                                                However, many drugs have entered clinics but so far showed limited efficacy, and we have limited understanding on why certain
                                                patients are non-responding. Even when there is an initial treatment response, cancer cells with high mutational potential and
                                                functional redundancy can easily develop drug resistance by activation of compensating pathways. To reach effective and sustained
                                                clinical responses, we critically need multi-targeted drug combinations, which shall selectively inhibit the cancer cells and block
                                                the emergence of drug resistance.

The Individualized Systems Medicine (ISM) platform at FIMM aims to identify novel therapeutic options that are most likely to be translated into clinics.
Cancer patient samples are collected from clinics and cultured for drug sensitivity testing and molecular profiling. Since exhaustive experimentation of all
 the possible drug combinations for each specific cancer type or patient is not possible, computational methods offer the improved efficiency to predict the
 most potential drug combinations.</p>



