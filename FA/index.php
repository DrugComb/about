												به منظور تسهیل یک طراحی منطقی از ترکیب چند دارو در راستای پزشکی فردی شده سرطان، ما یک روش خوشه بندی برای تعیین زیرگروه های بیماران بر مبنای درمان اختصاصی (داروی درست برای بیمار مشخص) توسعه دادیم. در این پژوهش، یک رویکرد مدلسازی شبکه ای برای مقاومت بیماران به شیمی درمانی توسعه داده شده است که بهترین ترکیب چند دارویی را پیش بینی می کند. پیش بینی ترکیب چند دارویی برای هر بیمار به صورت جداگانه انجام گرفته و به کمک پلتفرم آزمون دارو روی نمونه های بیماران پیش از درمانگاه، ارزیابی می گردد. 
سپس داده های به دست آمده به منظور تعیین هم¬افزایی قابل توجه در دوزهای درمانی مربوطه مورد بررسی قرار گرفته و به منظور تفسیر مکانیسم عمل، ترکیب چند دارویی موردنظر در شبکه پیام رسانی سرطان مدلسازی می شود. ترکیب های با بالاترین کارآمدی در هر نمونه بیمار یا زیرگروهی از بیماران برای استفاده در گزینه های درمانی اضافه می گردند. این فرایند پیش بینی، مدلسازی و آزمون ترکیب چند دارویی با توجه به مشکلات روش های درمانی رایج تک دارویی و دارای سمیت سلولی، می تواند منجر به روش درمانی جدید، ایمن تر و با کارایی بیشتر بگردد. 

												
