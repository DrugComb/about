<h3>研究策略</h3>

                                                <p align="justify">为了确保药物有持续的临床效果，我们需要针对多靶标进行组合用药，从而选择性抑制癌症细胞同时避免耐药性的发生。 FIMM的The Individualized Systems Medicine (ISM，个性化系统医疗平台) 旨在发掘可用于临床的全新的治疗方案。我们首先从临床采集癌症病人的样本，培养后测定样本的药物的敏感性和组学特征。然而，受条件限制，在每个病人样本上都进行所有药物组合的生物实验是不现实的。因此，我们开发了计算方法来预测最有潜力的药物组合，从而提高效率。
为了设计合理的个性化给药的药物组合，我们将开发基于模型的聚类方法将病人分类，不同类型病人采用不同治疗方法从而实现因人给药。 对于化学疗法耐药的病人，我们将建立网络模型来预测潜在的药物组合。
我们将为每个病人提供药物组合的个性化预测，然后把预测的潜在药物组合在临床前测试平台的病人样本上进行验证。测试后，我们基于药物组合筛选数据来进一步分析，以探索出治疗浓度下的最佳药物组合。预测的药物组合还将进一步与癌症信号网络整合，以探索其作用机制。最终那些对特定病人有选择性效果并经过临床前验证药物组合将被转化成治疗手段。相比于目前的细胞毒性药物和单靶标治疗药物，我们所提出来的药物组合预测、建模和测试流程将提供全新的、更有效的、更安全的治疗手段。
</p>


