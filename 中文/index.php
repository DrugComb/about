<h3>背景</h3>
                                                <p align="justify">更精准、更有效的癌症治疗是当今医疗系统的重大挑战之一。但是，目前许多临床药物的效果有限。而且，人类至今也无法解释个别病人对药物无效的现象。另一方面，即使在最开始有效，易突变的癌症细胞很容易通过激活补偿通路而产生耐药性，使得许多最开始有效的药物无效化。</p>



