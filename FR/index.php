<h3>Stratégie de Recherche</h3>

                                                <p align="justify">Afin de faciliter la conception rationnelle de combinaisons de médicaments permettant d’atteindre une médecine anticancéreuse réellement personnalisée, nous développerons des méthodes de classification pour l'identification de sous-groupes de patients nécessitant des traitement particuliers (« le bon médicament pour le bon patient »). Pour les patients résistant à la chimiothérapie, nous mettrons en place des approches de modélisation de réseaux d’interactions biologiques dans le but de prédire les combinaisons de médicaments qui seront les plus performantes. La prédiction des combinaisons de médicaments se fera pour chacun des patients, et sera validée à l'aide d'une plateforme qui évaluera ex vivo l’activité de ces combinaisons sur des échantillons prélevés des patients. Ensuite, nous explorerons les résultats de l’activité de ces combinaisons pour identifier d’éventuelles synergies significatives pour des doses thérapeutiques. Les meilleures combinaisons de médicaments testées seront aussi modélisées dans des réseaux de signalisation du cancer pour en déduire leurs mécanismes d'action. Ainsi, les combinaisons de médicaments présentant une efficacité sélective dans des échantillons de patients individuels ou dans des sous-groupes d'échantillons seront traduites en de potentielles options de traitement. Le pipeline complet proposé pour la prédiction, la modélisation, et l’effectuation de tests de combinaisons de médicaments pourrait alors déboucher sur de nouveaux traitements plus efficaces et plus sûrs par rapport aux monothérapies cytotoxiques actuelles.</p>


