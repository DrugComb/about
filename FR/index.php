<p align="justify">	DrugComb http://drugcomb.fimm.fi est un portail de données en libre accès, où les résultats des études de dépistage de combinaisons de médicaments pour une grande variété de lignées de cellules cancéreuses sont accumulés, normalisés et harmonisés. Une large sélection d'outils de visualisation et de calcul en expansion continue est fournie aux utilisateurs pour l'analyse des données de combinaison de médicaments. Toutes les données et tous les outils informatiques sont rendus gratuitement accessible à une vaste communauté de chercheurs sur le cancer.		</p>


