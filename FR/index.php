<h3>Contexte </h3>
                                                <p align="justify">Rendre le traitement du cancer plus personnalisé et plus efficace constitue l’un des plus grands défis de notre système de santé. Actuellement, de nombreux médicaments en usage clinique font preuve d’une efficacité très limitée auprès de certains groupes de patients, et cela pour des raisons toujours inconnues. Même en cas de réponse initiale au traitement, les cellules cancéreuses à potentiel mutationnel élevé et à redondance fonctionnelle peuvent facilement développer une pharmacorésistance par le biais d'une activation de multiples mécanismes de compensation. Par conséquent, la réalisation de réponses cliniques durables et efficaces nécessite des combinaisons de médicaments à cibles multiples, capables d’inhiber sélectivement les cellules cancéreuses et de bloquer ainsi l’émergence d’une résistance aux médicaments. La plateforme ‘Individualized Systems Medicine’ (ISM) [en français : la médecine des systèmes individualisés] à FIMM songe à identifier de nouvelles possibilités thérapeutiques susceptibles d'être traduits en usage clinique. Pour ce faire, des spécimens de patients atteints de cancer sont d’abord prélevés dans des cliniques et puis cultivés au laboratoire pour des tests de sensibilité aux médicaments et pour l'établissement de leurs profils moléculaires. Puisqu’il est impossible de tester de manière exhaustive toutes les combinaisons de médicaments pour chaque type de cancer ou pour chaque patient, la mise en œuvre de méthodes informatiques offre une efficacité accrue pour prévoir les combinaisons de médicaments les plus performantes.</p>



