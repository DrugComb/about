<h3>Strategia di ricerca</h3>

                                                <p align="justify">Per muoverci verso un futuro di medicina oncologica veramente personalizzata é innanzitutto necessario facilitare la progettazione razionale di combinazioni di farmaci. Con questo obiettivo, svilupperemo metodi di clustering per l'identificazione di sottogruppi omogenei di pazienti che richiedono trattamenti specifici ("il farmaco giusto per il paziente giusto"). Per i pazienti resistenti alla chemioterapia, svilupperemo modelli basati su reti per predire la combinazione di farmaci con la più alta probabilità di successo. La previsione della migliore combinazione di farmaci verrà effettuata per ciascun paziente e verrà convalidata utilizzando una piattaforma di test pre-clinica su campioni cellulari del paziente stesso. Esploreremo i dati ottenuti dallo screening delle combinazioni di farmaci per identificare eventuali sinergie significative a dosi terapeuticamente rilevanti. I risultati saranno modellati in reti di segnalazione cellulare del cancro per capire i meccanismi di azione. Le combinazioni di farmaci efficaci per singoli pazienti o gruppi di pazienti saranno infine tradotte in opzioni di trattamento. Gli approcci di previsione, modellizzazione e sperimentazione proposti hanno il potenziale di condurci a trattamenti nuovi, più efficaci e più sicuri rispetto alle attuali monoterapie citotossiche.</p>


