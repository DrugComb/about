<p align="justify">	DrugComb è un portale di dati ad accesso aperto che raccoglie i risultati di screening di combinazioni di farmaci tumorali. La base dati è composta da un'ampia varietà di linee cellulari tumorali ed i cui dati sono stati standardizzati ed armonizzati tra loro. È inoltre disponibile una ricca collezione, in continua espansione, di strumenti computazionali per la visualizzazione e l'analisi dei dati. Tutti i dati e gli strumenti informatici sono resi disponibili in modo libero e gratuito a beneficio dell'intera comunità di ricercatori oncologici. 		</p>


