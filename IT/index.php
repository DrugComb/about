<h3>Motivazioni</h3>
                                                <p align="justify">Rendere il trattamento del cancro più personalizzato ed efficace è una delle grandi sfide del nostro sistema sanitario. Tuttavia, molti farmaci che sono attualmente utilizzati in campo clinico hanno mostrato solamente una efficacia limitata ed il motivo per cui alcuni pazienti non rispondono positivamente al trattamento è ancora ignoto. Anche quando esiste un miglioramento delle condizioni come risposta al trattamento, le cellule tumorali con un alto tasso di mutazione e ridondanza funzionale possono facilmente sviluppare una resistenza ai farmaci mediante l'attivazione di percorsi compensatori. Per raggiungere delle terapie benefiche e durature nel tempo abbiamo perciò bisogno di combinazioni di farmaci a diversa tipologia di azione, con l'obiettivo di inibire selettivamente le cellule tumorali e bloccare l'emergere della resistenza ai farmaci. La piattaforma Sistemi di Medicina Individualizzata (ISM) di FIMM mira ad individuare nuove strategie terapeutiche con la più alta probabilità di successo in ambito clinico. I campioni cellulari di pazienti affetti da cancro sono raccolti in cliniche specializzate e successivamente coltivate per test di sensibilità a farmaci ed anche profilazione molecolare. Poiché la sperimentazione esaustiva di tutte le possibili combinazioni di farmaci per ogni specifico tipo di paziente non è possibile, i metodi computazionali offrono un efficace alternativa per predire le migliori potenziali combinazioni.</p>



