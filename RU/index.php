<h3>Стратегия исследования</h3>

                                                <p align="justify">Для упрощения рациональной разработки комбинаций лекарственных препаратов и де факто персонализированной противораковой терапии, мы работаем над идентификацией подгрупп пациентов, которым требуется индивидуальное лечение («правильное лекарство для правильного пациента»). Для пациентов устойчивых к химиотерапии мы разрабатываем подходы системного моделирования для предсказания потенциально наиболее эффективных комбинаций лекарственных препаратов. Предсказание подобных комбинаций будет сделано для каждого пациента с последующей проверкой на преклинической платформе тестирования лекарств. Мы изучим данные скрининга комбинаций лекарств для определения эффекта взаимного усиления терапевтического эффекта в терапевтически значимых диапазонах доз. Механизмы действия наиболее перспективных комбинаций лекарств будут смоделированы в сетях межклеточной коммуникации раковых опухолей. Комбинации лекарств с выборочно высокой эффективностью, проверенной на отдельных образцах пациентов или на их подгруппах, будут использованы для разработки клинических методов лечения. Мы надеемся, что данный метод прогнозирования комбинаций лекарственных средств приведет к открытию новых, более эффективных и безопасных методов лечения по сравнению с ныне существующими цитотоксическими монотерапиями.</p>


