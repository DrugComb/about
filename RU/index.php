<h3>Теоретические основы</h3>
                                                <p align="justify">На данный момент развитие более эффективных и, в будущем, персонализированных противораковых терапевтических средств является одной из главных задач в лечение раковых заболеваний. Большой процент лекарственных препаратов, поступающих в клиническую практику, имеет ограниченную эффективность, а научное общество не обладает достаточно глубоким пониманием того, почему определенные пациенты являются нон-респондентами. Даже при наличии первоначальной позитивной реакции на терапевтическое вмешательство, раковые клетки с высоким мутагенным потенциалом и функциональной избыточностью легко развивают иммунологическую толерантность путем активации компенсирующих метаболических путей. Для достижения эффективного и устойчивого клинического отклика необходимы комбинации лекарств способные селективно ингибировать раковые клетки, одновременно блокируя возникновение лекарственной резистентности. Платформа Индивидуализированной Системной Медицины (ISM) в Институте Молекулярной Медицины Финляндии (FIMM) ставит своей целью выявление новых комбинаций лекарств с максимально высоким потенциалом использования в клинических реалиях. Образцы раковых клеток пациентов собираются и культивируются in-vitro для тестирования восприимчивости к лекарственным препаратам и последующего молекулярного профилирования. Поскольку исчерпывающий экспериментальный анализ всех возможных комбинаций лекарств для каждого конкретного типа рака и/или пациента невозможен, необходимо использовать методы математического моделирования для повышения результативности прогнозирования эффективных комбинаций лекарств.</p>



